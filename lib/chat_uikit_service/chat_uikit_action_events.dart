/// Chat UI Kit Action Events
enum ChatUIKitEvent {
  /// Group id copied
  groupIdCopied,

  /// User id copied
  userIdCopied,

  /// Message copied
  messageCopied,

  /// Voice type not supported
  voiceTypeNotSupported,

  /// No microphone permission
  noMicrophonePermission,

  /// No camera permission
  noCameraPermission,

  /// No storage permission
  noStoragePermission,

  /// Message downloading
  messageDownloading,

  /// Message not found
  targetMessageNotFound,

  /// pin messages empty
  pinMessagesEmpty,

  /// record duration too short
  recordDurationTooShort,
}
